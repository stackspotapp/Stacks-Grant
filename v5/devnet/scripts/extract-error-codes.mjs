#!/usr/bin/env node
// Extract every `(define-constant ERR_XXX (err uNNN))` from contracts/*.clar,
// emit a Markdown report at docs/ERROR_CODES.md, and exit non-zero if two
// contracts share the same numeric code unless **all** sharing contracts
// implement `stackspot-trait` (the three pot variants).
//
//   Usage: node scripts/extract-error-codes.mjs           (writes report)
//          node scripts/extract-error-codes.mjs --check   (no write; exit code only)

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, basename, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CONTRACTS_DIR = join(repoRoot, "contracts");
const OUT = join(repoRoot, "docs", "ERROR_CODES.md");

const args = new Set(process.argv.slice(2));
const checkOnly = args.has("--check");

// Regex covers single- and multi-line forms produced by the Clarity formatter.
//   (define-constant ERR_X (err u1234))
//   (define-constant ERR_X
//     (err u1234)
//   )
// Captures: name, code.
const ERR_RE =
  /\(define-constant\s+(ERR_[A-Z0-9_]+)\s+\(err\s+u(\d+)\s*\)\s*\)/g;

// Trait we treat as the "pot family" — pot variants are allowed to share codes.
const POT_TRAIT_RE = /impl-trait\s+'?[^\s)]*stackspot-trait\.stackspot-trait/;

/** @typedef {{ contract: string, name: string, code: number, line: number }} Entry */

/** @returns {Entry[]} */
function extract(file) {
  const src = readFileSync(file, "utf8");
  const isPot = POT_TRAIT_RE.test(src);
  /** @type {Entry[]} */
  const out = [];
  // Reset regex state per file (it's a /g regex shared across calls).
  ERR_RE.lastIndex = 0;
  let m;
  while ((m = ERR_RE.exec(src)) !== null) {
    const before = src.slice(0, m.index);
    const line = before.split("\n").length;
    out.push({
      contract: basename(file, ".clar"),
      name: m[1],
      code: Number(m[2]),
      line,
    });
  }
  out.sort((a, b) => a.code - b.code || a.name.localeCompare(b.name));
  return { entries: out, isPot };
}

const files = readdirSync(CONTRACTS_DIR)
  .filter((f) => f.endsWith(".clar"))
  .map((f) => join(CONTRACTS_DIR, f))
  .sort();

/** @type {Record<string, { entries: Entry[], isPot: boolean }>} */
const byContract = {};
for (const f of files) {
  byContract[basename(f, ".clar")] = extract(f);
}

// Build code → list of {contract, name} for cross-contract analysis.
/** @type {Map<number, { contract: string, name: string, isPot: boolean }[]>} */
const byCode = new Map();
for (const [contract, { entries, isPot }] of Object.entries(byContract)) {
  for (const e of entries) {
    if (!byCode.has(e.code)) byCode.set(e.code, []);
    byCode.get(e.code).push({ contract, name: e.name, isPot });
  }
}

// Conflict = a code used in ≥2 contracts AND not all of them are pot-trait impls.
/** @type {{ code: number, uses: { contract: string, name: string, isPot: boolean }[] }[]} */
const conflicts = [];
for (const [code, uses] of [...byCode.entries()].sort((a, b) => a[0] - b[0])) {
  if (uses.length < 2) continue;
  const allPots = uses.every((u) => u.isPot);
  if (allPots) continue;
  conflicts.push({ code, uses });
}

// Optional check: pot-trait contracts that share a code SHOULD agree on the
// constant *name* — otherwise the same number means different things across
// pots, which defeats the "they're the same" rule.
/** @type {{ code: number, names: string[] }[]} */
const potNameMismatches = [];
for (const [code, uses] of byCode.entries()) {
  if (uses.length < 2) continue;
  const allPots = uses.every((u) => u.isPot);
  if (!allPots) continue;
  const names = [...new Set(uses.map((u) => u.name))];
  if (names.length > 1) potNameMismatches.push({ code, names: names.sort() });
}
potNameMismatches.sort((a, b) => a.code - b.code);

// ---------- Markdown output ----------------------------------------------

function table(rows) {
  if (rows.length === 0) return "_(none)_\n";
  const out = ["| Code | Constant | Line |", "|------|----------|------|"];
  for (const r of rows) out.push(`| u${r.code} | \`${r.name}\` | ${r.line} |`);
  return out.join("\n") + "\n";
}

const potContracts = Object.entries(byContract)
  .filter(([, v]) => v.isPot)
  .map(([k]) => k)
  .sort();

let md = "";
md += `# Error codes\n\n`;
md += `Generated by \`scripts/extract-error-codes.mjs\` on ${new Date().toISOString()}.\n\n`;
md += `Source: \`contracts/*.clar\`. ${Object.keys(byContract).length} contract(s) scanned, `;
md += `${[...byCode.values()].reduce((a, u) => a + u.length, 0)} \`ERR_*\` constant(s) found.\n\n`;
md += `**Pot-trait family** (allowed to share codes): ${potContracts.map((p) => `\`${p}\``).join(", ")}.\n\n`;

md += `## Per-contract listings\n\n`;
for (const c of Object.keys(byContract).sort()) {
  const { entries, isPot } = byContract[c];
  if (entries.length === 0) continue;
  md += `### \`${c}.clar\`${isPot ? " — *pot-trait impl*" : ""}\n\n`;
  md += table(entries) + "\n";
}

md += `## Cross-contract analysis\n\n`;
md += `### Conflicts\n\n`;
md += `A *conflict* is a numeric code used in two or more contracts where at least one of them is **not** a pot-trait implementation.\n\n`;
if (conflicts.length === 0) {
  md += `No conflicts. ✅\n\n`;
} else {
  for (const c of conflicts) {
    md += `- **u${c.code}**: `;
    md += c.uses
      .map((u) => `\`${u.contract}\`::\`${u.name}\`${u.isPot ? " *(pot)*" : ""}`)
      .join(", ");
    md += "\n";
  }
  md += "\n";
}

md += `### Pot-family code reuse\n\n`;
md += `Codes shared across the pot-trait implementations (allowed by policy):\n\n`;
const sharedPotCodes = [...byCode.entries()]
  .filter(([, uses]) => uses.length >= 2 && uses.every((u) => u.isPot))
  .sort((a, b) => a[0] - b[0]);
if (sharedPotCodes.length === 0) {
  md += `_(none)_\n\n`;
} else {
  md += `| Code | Constant | Pots |\n|------|----------|------|\n`;
  for (const [code, uses] of sharedPotCodes) {
    const names = [...new Set(uses.map((u) => u.name))];
    const constant = names.length === 1 ? `\`${names[0]}\`` : names.map((n) => `\`${n}\``).join(" / ");
    md += `| u${code} | ${constant} | ${uses.map((u) => `\`${u.contract}\``).join(", ")} |\n`;
  }
  md += "\n";
}

md += `### Pot-family name mismatches\n\n`;
md += `Pot contracts SHOULD reuse the same *name* for a shared code; otherwise the number means different things across pots.\n\n`;
if (potNameMismatches.length === 0) {
  md += `No mismatches. ✅\n\n`;
} else {
  for (const m of potNameMismatches) {
    md += `- **u${m.code}**: ${m.names.map((n) => `\`${n}\``).join(", ")}\n`;
  }
  md += "\n";
}

if (!checkOnly) {
  writeFileSync(OUT, md);
  process.stdout.write(`Wrote ${OUT}\n`);
}

// ---------- Console summary + exit code ----------------------------------
const total = [...byCode.values()].reduce((a, u) => a + u.length, 0);
process.stdout.write(
  `Scanned ${Object.keys(byContract).length} contract(s); ${total} ERR_* constants; ` +
    `${conflicts.length} conflict(s); ${potNameMismatches.length} pot-name mismatch(es).\n`,
);

if (conflicts.length > 0) {
  process.stdout.write(`\nCONFLICTS:\n`);
  for (const c of conflicts) {
    process.stdout.write(
      `  u${c.code}: ` +
        c.uses.map((u) => `${u.contract}::${u.name}${u.isPot ? "*" : ""}`).join(", ") +
        "\n",
    );
  }
}
if (potNameMismatches.length > 0) {
  process.stdout.write(`\nPOT NAME MISMATCHES:\n`);
  for (const m of potNameMismatches) {
    process.stdout.write(`  u${m.code}: ${m.names.join(", ")}\n`);
  }
}

process.exit(conflicts.length > 0 ? 1 : 0);
